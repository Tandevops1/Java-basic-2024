package com.java;

public class SwitchTopics {
    public static void main(String[] args) {


        int number = 4;

        switch (number) {
            case 1:
                System.out.println("samosa");
                break;
            case 2:
                System.out.println("Mango shake");
                break;
            case 3:
                System.out.println("burger");
                break;
            case 4:
                System.out.println("coffee");
                break;
            default:
                System.out.println("wake up");
        }
    }

    }
